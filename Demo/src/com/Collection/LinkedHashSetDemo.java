package com.Collection;

import java.util.LinkedHashSet;
import java.util.Set;

public class LinkedHashSetDemo {

	public static void main(String[] args) {
		Set<String> s=new LinkedHashSet<String>();
		s.add("Ron");
		s.add("Sam");
		s.add("Karan");
		s.add("Ben");
		
		System.out.println(s);
		

	}

}
