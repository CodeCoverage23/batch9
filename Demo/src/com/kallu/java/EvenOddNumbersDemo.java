package com.kallu.java;

public class EvenOddNumbersDemo {
	 
  
	public static void main(String[] args) {
		

		 for(int i=1;i<=1000;i++) {
			 if(i%2==0) {
				 System.out.println("Even Number");
			 }else {
				 System.out.println("Odd Number");
				 
			 }
			 System.out.println(i);
			 }
		}
		}
	