package com.assignments_6.java;
// print all even number from 50 to 75.
public class Assignment6_3 {
	
	public static void main(String[] args) {
		
		
		System.out.println("Even number between 50 to 75: ");
		
		
		for(int x = 50; x <= 75; x++) {
		
		if(x % 2 == 0) {
			System.out.println(x);
		}
		
		}
		
	}
}
