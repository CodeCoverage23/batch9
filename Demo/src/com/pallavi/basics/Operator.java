package com.pallavi.basics;

public class Operator {

	public static void main(String[] args) {
		
		int num = 7;
		num++; // post increment
		       //fetch the value and then increment
		
		System.out.println(num);
		
		int num1 = 7;
		++num; // pre-increment 
		       // increment and then fetch the value
		System.out.println(num1);
		
	
   }

  }
