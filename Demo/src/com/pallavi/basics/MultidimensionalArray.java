package com.pallavi.basics;

public class MultidimensionalArray {
	public static void main(String[] args) {
		int array [][]  = {{3, 5, 8},{7, 54, 1, 12, 4}};
		
		System.out.println( array[0][2]); // array[0] refer to the first row of the array
		                                  //array[0][2] access the element 2nd index first row thats why the output is 8
	}

}
