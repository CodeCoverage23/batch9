package com.basic;

public class DataTypesDemo {

	byte b;
	short s;
	int i;
	long l;
	float f;
	static double d;
	boolean b1;
	char c;

	public static void main(String[] args) {

		float e = 11.2f;
		System.out.println(e);

		double g = 120.20d;

		char ch = 'S';
		System.out.println(ch);

		boolean t = true;
		System.out.println(t);

		System.out.println(d);

		int a = 100;
		++a;
		System.out.println(a);
		--a; // 99 or 100
		System.out.println(a);

	}

}
